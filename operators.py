# Operator adalah simbol khusus yang digunakan untuk melakukan operasi tertentu pada suatu nilai atau variable.

# Arithmetic Operators (Aritmatika)
# Addition +
result = 1 + 2

# Subtraction -
result = 2 - 1

# Multiplication *
result = 2 * 3

# Division /
result = 4 / 3 # float

# Floor Division //
result = 4 // 3

# Modulus % (reminder) 
result = 10 % 3

# Exponent (**) -> pangkat
result = 4 ** 3
# print(result)

